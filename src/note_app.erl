-module(note_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    io:format("Starting the note app"),
    note_store:init(),
    note_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

